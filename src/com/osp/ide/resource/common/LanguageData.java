package com.osp.ide.resource.common;

public class LanguageData {

	String id="";
	String name="";
	
	public LanguageData()
	{
		
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	
	public String getName()
	{
		return name;
	}
	
	
	public void setId(String id)
	{
		this.id = id;
	}
	
	public String getId()
	{
		return id;
	}
}
